package com.pa3.superClient;

public class NeighborPeers {
	
	String peerId;
	int portno;
	String ip;
	
	
	public String getPeerId() {
		return peerId;
	}
	public void setPeerId(String peerId) {
		this.peerId = peerId;
	}
	public int getPortno() {
		return portno;
	}
	public void setPortno(int portno) {
		this.portno = portno;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}

}
